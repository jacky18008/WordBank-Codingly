def search_dict(wordbank, query):
    # A function to find the relative wordbank by a word.
    # However, for basic use, you only need to call the "add_dict" function below.
    
    for key in wordbank:
        if key == query:
            return wordbank[key]
        else:
            search_dict(wordbank[key], query)


